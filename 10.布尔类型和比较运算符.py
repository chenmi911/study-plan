# ==判断内容是否相等，满足为true，不满足为false
# !=判断内容是否不相等
bool_1 = True
bool_2 = False
print(f"bool_1变量的内容是:{bool_1}, 类型是{bool_1}")
print(f"bool_2变量的内容是:{bool_2}, 类型是{bool_2}")
name_a = "i am a"
name_b = "i am b"
print(f"name_a == name_b的结果是：{name_a == name_b}")

name_a = "i am a"
name_b = "i am b"
print(f"name_a != name_b的结果是：{name_a != name_b}")

